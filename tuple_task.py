tup = ('С', 'а', 'з', 'ы', 'к', 'и', 'н', ' ', 'Л', 'е', 'в', ' ', 'Ю', 'р', 'ь', 'е', 'в', 'и', 'ч')

print("Длина кортежа = ", len(tup), "\n")
print("Все элементы кортежа от 15 до конца: \n", tup[15:], "\n")

for i in ('з', 'ш', 'л', 'к', 'п', 'ц', 'ж'):
    if i in tup:
        print(i, "есть в кортеже")
    else:
        print(i, "нет в кортеже")
